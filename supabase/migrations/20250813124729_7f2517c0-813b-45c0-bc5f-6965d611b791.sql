-- Fix Function Search Path Security Issues
-- Update all functions to have SET search_path = '' for security

CREATE OR REPLACE FUNCTION public.handle_prompt_history_updated_at()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path = ''
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

